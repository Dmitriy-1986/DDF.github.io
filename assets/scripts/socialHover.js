/* Facebook */
facebookLink.onmouseover = function () {
    let blok = document.getElementById('facebook');
    blok.style.borderColor = "#4267b2";
    blok.style.boxShadow = "0px 0px 20px #4267b2";
    blok.style.textShadow = "0px 0px 20px #4267b2";
    blok.style.color = "#4267b2";
    
    blok.style.transition = '2s';

    let black = document.getElementById('facebookLink');
    black.style.textShadow = "0px 0px 20px #4267b2";
    black.style.color = "#4267b2";
}

facebookLink.onmouseout = function () {
    let blok = document.getElementById('facebook');
    blok.style.borderColor = '#66696c';
    blok.style.color = '#66696c';
    blok.style.boxShadow = "none";
    blok.style.textShadow = "none";

    let black = document.getElementById('facebookLink');
    black.style.color = '#66696c';
    black.style.boxShadow = "none";
    black.style.textShadow = "none";
}

